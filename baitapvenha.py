def toan_chan(a):
    for row in a:
        for val in row:
            if val % 2 != 0:
                return False
    return True
#2e
def doi_xung(a):
    n = len(a)
    for i in range(n):
        for j in range(n):
            if a[i][j] != a[j][i]:
                return False
    return True
#2f
def cheo_chinh_tang(a):
    n = len(a)
    for i in range(n - 1):
        if a[i][i] >= a[i + 1][i + 1]:
            return False
    return True
