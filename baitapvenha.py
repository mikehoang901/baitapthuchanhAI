#Bai1
def tron_mang(a, b):
    c = []
    n = min(len(a), len(b))

    # Lấy min từng cặp
    for i in range(n):
        c.append(min(a[i], b[i]))

    # Nếu a còn phần tử
    if len(a) > n:
        c.extend(a[n:])

    # Nếu b còn phần tử
    if len(b) > n:
        c.extend(b[n:])

    return c


