def tong_tam_giac_tren_cheo_phu(a):
    n = len(a)
    tong = 0
    for i in range(n):
        for j in range(n):
            if i + j <= n - 1:  # phía trên (kể cả chéo phụ)
                tong += a[i][j]
    return tong
