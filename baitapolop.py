class SinhVien:
    def __init__(self, ma, ten, nam_sinh, diem):
        self.ma = ma
        self.ten = ten
        self.nam_sinh = nam_sinh
        self.diem = diem

# b. Nhập danh sách
def nhap_ds(n):
    ds = []
    for _ in range(n):
        ma = input("Mã SV: ")
        ten = input("Tên: ")
        nam = int(input("Năm sinh: "))
        diem = float(input("Điểm TB: "))
        ds.append(SinhVien(ma, ten, nam, diem))
    return ds

# c. Đếm SV đủ điều kiện lên lớp (>=5)
def dem_len_lop(ds):
    return sum(1 for sv in ds if sv.diem >= 5)

# d. Xuất SV đủ 20 tuổi
def sv_20_tuoi(ds, nam_hien_tai=2025):
    for sv in ds:
        if nam_hien_tai - sv.nam_sinh == 20:
            print(sv.ten)

# e. Đếm SV hệ ĐH (ký tự 2-3 là "DH")
def dem_he_dh(ds):
    return sum(1 for sv in ds if sv.ma[2:4] == "DH")

# f. Đếm tên "Lan"
def dem_ten_lan(ds):
    return sum(1 for sv in ds if "Lan" in sv.ten)

# g. Đếm họ "Phan"
def dem_ho_phan(ds):
    return sum(1 for sv in ds if sv.ten.startswith("Phan"))


