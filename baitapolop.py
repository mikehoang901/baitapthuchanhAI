# a. Tạo mảng ngẫu nhiên
def tao_mang(m, n):
    return [[random.randint(0, 50) for _ in range(n)] for _ in range(m)]

# In mảng
def xuat_mang(a):
    for row in a:
        print(row)

# b. Xuất dòng k
def xuat_dong(a, k):
    print(a[k])

# c. Xuất cột k
def xuat_cot(a, k):
    for row in a:
        print(row[k])

# d. Tìm dòng có tổng > 45 lớn nhất
def dong_tong_lon_nhat(a):
    max_sum = -1
    index = -1
    for i in range(len(a)):
        s = sum(a[i])
        if s > 45 and s > max_sum:
            max_sum = s
            index = i
    return index, max_sum

# e. Tìm cột có tích nhỏ nhất
def cot_tich_nho_nhat(a):
    n = len(a[0])
    min_tich = float('inf')
    index = -1
    for j in range(n):
        tich = 1
        for i in range(len(a)):
            tich *= a[i][j]
        if tich < min_tich:
            min_tich = tich
            index = j
    return index, min_tich

# f. Xuất phần tử dòng chẵn, cột lẻ
def dong_chan_cot_le(a):
    for i in range(len(a)):
        if i % 2 == 0:
            for j in range(len(a[0])):
                if j % 2 == 1:
                    print(a[i][j], end=" ")
    print()

# g. TBC phần tử chẵn thuộc dòng lẻ
def tbc_chan_dong_le(a):
    tong = dem = 0
    for i in range(len(a)):
        if i % 2 == 1:
            for x in a[i]:
                if x % 2 == 0:
                    tong += x
                    dem += 1
    return tong / dem if dem != 0 else 0

# h. TBC phần tử biên
def tbc_bien(a):
    m, n = len(a), len(a[0])
    tong = dem = 0
    for i in range(m):
        for j in range(n):
            if i == 0 or i == m-1 or j == 0 or j == n-1:
                tong += a[i][j]
                dem += 1
    return tong / dem

# i. TBC phần tử không thuộc biên
def tbc_khong_bien(a):
    m, n = len(a), len(a[0])
    tong = dem = 0
    for i in range(1, m-1):
        for j in range(1, n-1):
            tong += a[i][j]
            dem += 1
    return tong / dem if dem != 0 else 0


